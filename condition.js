//conditional statements (if else)
var x=5
//only if statement
if(x==5)
{
    console.log("The value is 5");
}


var y =6;
//check y is even or odd
if(y%2==0)
{
    console.log(y," is even ");
}
else
{
    console.log(y ," is odd ");

}

var z=-5;
