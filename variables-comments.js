//variables can be of two types var and const 
//earlier it was only let 


//let variable can be changed after declaration 
let a =5 ;
console.log(a);
a= a+5; //changing the value of a 
console.log(a);


//Var type (can be changed whenever wanted after declaration )
var b =10 ;
console.log(b);

b=b*10;
console.log(b);

//const type (assigned at the time of declaration)
const c=10;
console.log(c);
//c = c-2;
//changes of c are not allowed as it is of const type
